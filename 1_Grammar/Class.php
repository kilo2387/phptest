/*
 * 类：1、具有同种属性的对象称为类，是个抽象的概念。比如说：汽车、人、狗、神；2、抽取同类实体的共同性自定义的一种数据类型称之为类
 * 类的实质是一种数据类型，类似于int、char等基本类型，不同的是它是一种复杂的数据类型。
 * 因为它的本质是类型，而不是数据，所以不存在于内存中，不能被直接操作，只有被实例化为对象时，才会变得可操作。
 * 类的内部封装了方法，用于操作自身的成员。
 * 类是对某种对象的定义，具有行为(be-havior)，它描述一个对象能够做什么以及做的方法(method)，它们是可以对这个对象进行操作的程序和过程。
 * 它包含有关对象行为方式的信息，包括它的名称、方法、属性和事件。
 * 类的构成包括数据成员和成员函数。
 * 数据成员对应类的属性，类的数据成员也是一种数据类型，并不需要分配内存。
 * 成员函数则用于操作类的各项属性，是一个类具有的特有的操作，比如“学生”可以“上课”，而“水果”则不能。类和外界发生交互的操作称为接口。
